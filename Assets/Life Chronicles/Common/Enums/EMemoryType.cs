public enum MemoryType
{
    Interaction,       // Interação com outro NPC
    Event,             // Evento significativo (casamento, nascimento, morte)
    Achievement,       // Conquista pessoal
    Observation,       // Algo que o NPC observou
    Experience,        // Experiência geral (trabalho, viagem)
    Trauma,            // Experiência negativa significativa
    Joy,               // Experiência positiva significativa
    Learning,          // Algo que o NPC aprendeu
    Relationship,      // Mudança em um relacionamento
    Decision           // Decisão importante tomada
}